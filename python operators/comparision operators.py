'''Comparison (==, !=, >, <, >=, <=)
Ask the user for two numbers.
Compare them using all comparison operators.
print results as True/False.'''
a=int(input("enter value of a:"))
b=int(input("enter value of b:"))
print("a==b:",a==b)
print("a!=b:",a!=b)
print("a>b:",a>b)
print("a<b:",a<b)
print("a>=b:",a>=b)
print("a<=b:",a<=b)

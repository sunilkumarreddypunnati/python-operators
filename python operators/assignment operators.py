'''Assignment (+=, -=, *=, /=, //=, %=, =)
Start with num = 100.
Apply each assignment operator step by step.
print the result after each operation.'''
num=int(input("enter a value:"))
num+=100
print(num)
num-=100
print(num)
num*=2
print(num)
num/=5
print(num)
num//=5
print(num)
num%=5
print(num)
num**=2
print(num)


